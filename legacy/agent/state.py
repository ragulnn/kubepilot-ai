from typing import TypedDict, Any


class AgentState(TypedDict):

    # --------------------------------------------------
    # User Input
    # --------------------------------------------------

    question: str

    # --------------------------------------------------
    # Planner
    # --------------------------------------------------

    current_action: dict | None

    # --------------------------------------------------
    # Investigation
    # --------------------------------------------------

    observations: list

    investigation: dict | None

    # --------------------------------------------------
    # Resource Layer
    # --------------------------------------------------

    resources: dict

    # --------------------------------------------------
    # Evidence Layer
    # --------------------------------------------------

    evidence: list

    # --------------------------------------------------
    # Knowledge Graph
    # --------------------------------------------------

    knowledge_graph: Any

    # --------------------------------------------------
    # Root Cause Analysis
    # --------------------------------------------------

    root_causes: list

    confidence: float

    recommended_action: dict | None

    # --------------------------------------------------
    # Final Report
    # --------------------------------------------------

    answer: dict | str

    # --------------------------------------------------
    # Memory
    # --------------------------------------------------

    fingerprint: dict | None

    memory_match: dict | None

    # --------------------------------------------------
    # Investigation Status
    # --------------------------------------------------

    finished: bool

    guard_reason: str
